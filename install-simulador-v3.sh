id www-data >/dev/null 2>&1 && WEBUSER="www-data:www-data"
id apache  >/dev/null 2>&1 && WEBUSER="apache:apache"
id nginx   >/dev/null 2>&1 && WEBUSER="nginx:nginx"

mkdir -p "$CONS_DIR" "$SUP_DIR"
TMPZIP=$(mktemp --suffix=.zip)

