set -euo pipefail

echo "================================================================"
echo "  Simulador de Premiacao - Instalador ADS Software (v3)"
echo "  Instala duas pastas separadas: consultor e supervisor"
echo "================================================================"

# 1) Detectar gerenciador de pacotes
if command -v apt >/dev/null 2>&1; then
  export DEBIAN_FRONTEND=noninteractive
  echo "==> Usando APT (Debian/Ubuntu)"
  apt-get update -qq
  apt-get install -y -qq apache2 unzip curl >/dev/null
elif command -v dnf >/dev/null 2>&1; then
  echo "==> Usando DNF"
  dnf install -y -q httpd unzip curl >/dev/null
elif command -v yum >/dev/null 2>&1; then
  echo "==> Usando YUM"
  yum install -y -q httpd unzip curl >/dev/null
fi

